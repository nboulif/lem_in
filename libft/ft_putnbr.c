#include <unistd.h>

void	ft_putnbr(int n)
{
	char c;
	long nl;

	nl = n;
	if (nl < 0)
	{
		nl = -nl;
		write(1, "-", 1);
	}
	if (nl >= 10)
		ft_putnbr(nl / 10);
	c = nl % 10 + '0';
	write(1, &c, 1);
}

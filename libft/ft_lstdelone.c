#include "libft.h"
#include <stdlib.h>

void	ft_lstdelone(t_list **alst, void (*del)(void*, size_t))
{
	if (alst && *alst != NULL && del)
	{
		(*del)(*alst, alst[0]->content_size);
		alst[0] = NULL;
	}
}

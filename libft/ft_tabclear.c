#include <stdlib.h>
#include "libft.h"

void	ft_tabclear(char ***tab)
{
	int i;

	i = 0;
	if (*tab)
	{
		while (*tab)
		{
			if (**tab)
				free(**tab);
			*tab += 1;
			i++;
		}
	}
	free((*tab - i));
}
